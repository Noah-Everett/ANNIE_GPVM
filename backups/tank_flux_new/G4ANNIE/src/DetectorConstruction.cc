DetectorConstruction::DetectorConstruction(G4String gdmlfname)
  : GDMLFilename(gdmlfname)
  , doOverlapCheck(false)
  , magField(0)
  , worldVol(0)
  , detectorMessenger(0)
{
  
  // create commands for interactive definition of the calorimeter
  detectorMessenger = new DetectorMessenger(this);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

DetectorConstruction::~DetectorConstruction()
{ delete detectorMessenger;}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

G4VPhysicalVolume* DetectorConstruction::Construct()
{
  return ConstructIt();
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......
void DetectorConstruction::SetOverlapCheck(G4String flag)
{
  if      ( flag == "on"    ) doOverlapCheck = 1;
  else if ( flag == "off"   ) doOverlapCheck = 0;
  else if ( flag == "true"  ) doOverlapCheck = 1;
  else if ( flag == "false" ) doOverlapCheck = 0;
  else if ( flag == "1"     ) doOverlapCheck = 1;
  else if ( flag == "0"     ) doOverlapCheck = 0;
  else {
    G4cout << "SetOverlapCheck was passed \"" << flag << "\"" << G4endl;
    doOverlapCheck = 0;
  }
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

G4VPhysicalVolume* DetectorConstruction::ConstructIt()
{

  // Clean old geometry, if any
  //
  G4GeometryManager::GetInstance()->OpenGeometry();
  G4PhysicalVolumeStore::GetInstance()->Clean();
  G4LogicalVolumeStore::GetInstance()->Clean();
  G4SolidStore::GetInstance()->Clean();

  //
  // Read GDML file
  //
  G4GDMLParser parser;
  parser.SetOverlapCheck( doOverlapCheck );
  G4cout << "Read " << GDMLFilename 
         << " (overlap check = " << (doOverlapCheck?"true":"false") << ")"
         << G4endl;
  parser.Read ( GDMLFilename );

  //
  worldVol = parser.GetWorldVolume();

  //                                        
  // Visualization attributes
  //
  G4LogicalVolume* logicWorld = worldVol->GetLogicalVolume();
  logicWorld->SetVisAttributes (G4VisAttributes::Invisible);

  G4VisAttributes* simpleBoxVisAtt= new G4VisAttributes(G4Colour(1.0,1.0,1.0));
  simpleBoxVisAtt->SetVisibility(true);
  //logicCalor->SetVisAttributes(simpleBoxVisAtt);

  //
  //always return the physical World
  //
  return worldVol;
}

